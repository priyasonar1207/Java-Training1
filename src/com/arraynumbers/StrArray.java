package com.arraynumbers;

public class StrArray {

	public static void main(String[] args) {
		
		String[] strCity = new String[4];  
		  
		strCity[0] = "Delhi";  
		strCity[1] = "Jaipur";  
		strCity[2] = "Gujarat";  
		strCity[3] = "Mumbai";  
		 
		for (String str : strCity)   
		{  
		System.out.println(str);  
		}  
		}  
		}  

		
