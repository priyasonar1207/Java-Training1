package com.oops_encapsulation;

public class CollegeDetails {

	private int intRollNo;
	private String strStudentName;
	private int intAge;
	private String strCityName;
	
	
	public int getIntRollNo() {
		return intRollNo;
	}
	public void setIntRollNo(int intRollNo) {
		this.intRollNo = intRollNo;
	}
	
	public String getStrStudentName() {
		return strStudentName;
	}
	public void setStrStudentName(String strStudentName) {
		this.strStudentName = strStudentName;
	}
	
	public int getIntAge() {
		return intAge;
	}
	public void setIntAge(int intAge) {
		this.intAge = intAge;
	}
	
	public String getStrCityName() {
		return strCityName;
	}
	public void setStrCityName(String strCityName) {
		this.strCityName = strCityName;
	}
	
}
	
	
	
	
	
	


